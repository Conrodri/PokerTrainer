/** Resolve the open-raise table for a given table format and game type. */
function openTable(format: TableFormat = '6max', gameType: GameType = 'cashgame'): Record<string, RangeMatrix> {
  if (format === 'hu')   return gameType === 'mtt' ? OPEN_RAISE_HU_MTT  : OPEN_RAISE_HU_CG;
  if (format === '3max') return gameType === 'mtt' ? OPEN_RAISE_3MAX_MTT : OPEN_RAISE_3MAX_CG;
  if (gameType === 'mtt') {
    return format === '8max' ? OPEN_RAISE_8MAX_MTT : OPEN_RAISE_6MAX_MTT;
  }
  return format === '8max' ? OPEN_RAISE_8MAX : OPEN_RAISE;
}

export function getMatrixIndices(notation: string): [number, number] {
  if (notation.length === 2) {
    // Pair: AA, KK...
    const idx = RANKS_DISPLAY.indexOf(notation[0]);
    return [idx, idx];
  }
  const r1 = notation[0];
  const r2 = notation[1];
  const suited = notation[2] === 's';
  const r1Idx = RANKS_DISPLAY.indexOf(r1);
  const r2Idx = RANKS_DISPLAY.indexOf(r2);

  if (suited) {
    return [r1Idx, r2Idx]; // upper triangle
  } else {
    return [r2Idx, r1Idx]; // lower triangle (higher rank in col)
  }
}

export function getRangeFrequency(position: Position8, notation: string, format: TableFormat = '6max', gameType: GameType = 'cashgame'): number {
  const [row, col] = getMatrixIndices(notation);
  const matrix = openTable(format, gameType)[position];
  if (!matrix || row < 0 || col < 0) return 0;
  return matrix[row][col];
}

export function getCorrectAction(position: Position8, notation: string, format: TableFormat = '6max', gameType: GameType = 'cashgame'): {
  action: 'raise' | 'call' | 'fold';
  frequency: number;
  isMixed: boolean;
} {
  const freq = getRangeFrequency(position, notation, format, gameType);
  // 0 → fold | 0 < v ≤ 0.5 → mixed (displayed as call) | v > 0.5 → raise
  const action = freq === 0 ? 'fold' : freq > 0.5 ? 'raise' : 'call';
  return { action, frequency: freq, isMixed: false };
}

export function getRangeMatrix(position: Position8, format: TableFormat = '6max', gameType: GameType = 'cashgame'): RangeMatrix {
  const table = openTable(format, gameType);
  return table[position] || table.HJ;
}

export function getRangePercentage(position: Position8, format: TableFormat = '6max', gameType: GameType = 'cashgame'): number {
  const matrix = openTable(format, gameType)[position];
  let total = 0;
  let inRange = 0;
  for (let i = 0; i < 13; i++) {
    for (let j = 0; j < 13; j++) {
      const freq = matrix[i][j];
      const suitedCombos = 4;
      const offsuitCombos = 12;

      if (i === j) {
        total += 6;
        inRange += freq * 6;
      } else if (i < j) {
        total += suitedCombos;
        inRange += freq * suitedCombos;
      } else {
        total += offsuitCombos;
        inRange += freq * offsuitCombos;
      }
    }
  }
  return Math.round((inRange / total) * 100 * 10) / 10;
}
